package com.example.barcarena;

public class problema {
    private String titulo;
    private int imagem;

    public problema(String titulo, int imagem) {
        this.titulo = titulo;
        this.imagem = imagem;
    }

    public String getTitulo() {
        return titulo;
    }

    public int getImagem() {
        return imagem;
    }
}

